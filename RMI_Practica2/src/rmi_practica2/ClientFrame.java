    private void jt_Panel_FilesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_Panel_FilesMouseClicked
        // TODO add your handling code here:
        try {
            if (loggedUser) {
                    //JOptionPane.showMessageDialog(this, "what");
                    int row = jt_Panel_Files.getClosestRowForLocation(evt.getX(), evt.getY());
                    jt_Panel_Files.setSelectionRow(row);
                    Object v1
                            = jt_Panel_Files.getSelectionPath().
                                    getLastPathComponent();
                    nodo_seleccionado = (DefaultMutableTreeNode) v1;
                    if (nodo_seleccionado.getUserObject() instanceof File) {
                        String data = nodo_seleccionado.toString();
                        //JOptionPane.showMessageDialog(this, data);
                        filePath=data;
                    }
                
            } else {
                JOptionPane.showMessageDialog(this, "Already logged in");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jt_Panel_FilesMouseClicked

